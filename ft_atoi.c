#include "printf.h"

int		ft_atoi(const char *str)
{
	size_t			i;
	int				s;
	long long int	r;

	r = 0;
	s = 1;
	i = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'\
			|| str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	while (str[i] == '-' || str[i] == '+')
		if (str[i++] == '-')
			s = -1;
	if (str[i] != '\0')
		while (str[i] != '\0' && str[i] >= 48 && str[i] <= 57)
			r = r * 10 + str[i++] - 48;
	if (r == 2147483648)
		return (-2147483648);
	if (r < -2147483648 || r > 2147483647)
		return ((s == 1) ? -1 : 0);
	return (s * r);
}
